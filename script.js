const form = document.querySelector("#booking-form");
const preview = document.querySelector("#message-preview");
const serviceSelect = document.querySelector("#service");
const langButtons = document.querySelectorAll(".lang-button");
const fields = ["name", "phone", "vehicle", "service", "day", "details"].map((id) =>
  document.querySelector(`#${id}`)
);

let currentLang = "en";

function renderServiceOptions() {
  const { servicePlaceholder, services } = translations[currentLang];
  const selected = serviceSelect.value;

  serviceSelect.innerHTML = "";

  const placeholder = document.createElement("option");
  placeholder.value = "";
  placeholder.textContent = servicePlaceholder;
  serviceSelect.appendChild(placeholder);

  services.forEach((service) => {
    const option = document.createElement("option");
    option.value = service;
    option.textContent = service;
    serviceSelect.appendChild(option);
  });

  serviceSelect.value = services.includes(selected) ? selected : "";
}

function buildMessage() {
  const values = Object.fromEntries(
    fields.map((field) => [field.id, field.value.trim()])
  );
  const labels = translations[currentLang].previewLines;

  return [
    labels[0],
    `${labels[1]}: ${values.name}`,
    `${labels[2]}: ${values.phone}`,
    `${labels[3]}: ${values.vehicle}`,
    `${labels[4]}: ${values.service}`,
    `${labels[5]}: ${values.day}`,
    `${labels[6]}: ${values.details}`
  ].join("\n");
}

function updatePreview() {
  preview.textContent = buildMessage();
}

function applyTranslations() {
  document.documentElement.lang = currentLang;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (translations[currentLang][key]) {
      element.textContent = translations[currentLang][key];
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    element.placeholder = translations[currentLang][key];
  });

  renderServiceOptions();
  updatePreview();

  langButtons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === currentLang);
  });
}

fields.forEach((field) => {
  field.addEventListener("input", updatePreview);
  field.addEventListener("change", updatePreview);
});

langButtons.forEach((button) => {
  button.addEventListener("click", () => {
    currentLang = button.dataset.lang;
    applyTranslations();
  });
});

form.addEventListener("submit", (event) => {
  event.preventDefault();
  const message = buildMessage();
  window.location.href = `sms:+12244044798?&body=${encodeURIComponent(message)}`;
});

applyTranslations();
